package com.Hotel;

public class HotelBreakfast {
	public int HotelID;
	public String description;
	public float price;
	public String type;
	
	public HotelBreakfast(int HotelID, String description, float price, String type){
		this.HotelID = HotelID;
		this.description = description;
		this.price = price;
		this.type=  type;
	}
}
